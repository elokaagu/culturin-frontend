const byContinent: Record<CountryExplorerContinent, MajorCity[]> = {
  europe,
  asia,
  africa,
  northAmerica,
  southAmerica,
};

export function getMajorCitiesByContinent(continent: CountryExplorerContinent): MajorCity[] {
  return byContinent[continent] ?? [];
}

function getAllMajorCitiesInWorld(): MajorCity[] {
  return [...europe, ...asia, ...africa, ...northAmerica, ...southAmerica].sort((a, b) =>
    a.name.localeCompare(b.name, "en"),
  );
}

export const totalMajorCityCount: number = getAllMajorCitiesInWorld().length;
