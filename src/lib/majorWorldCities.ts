const dedupe = (list: MajorCity[], key: (m: MajorCity) => string): MajorCity[] => {
  const seen = new Set<string>();
  return list.filter((m) => {
    const k = key(m);
    if (seen.has(k)) return false;
    seen.add(k);
    return true;
  });
};

